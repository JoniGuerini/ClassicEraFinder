local reverseByLocale = {}
local displayCache = {}

local function buildReverse(localeCode)
  local pack = CEF.AREA_DISPLAY_NAMES and CEF.AREA_DISPLAY_NAMES[localeCode]
  if not pack then
    return {}
  end
  local parents = CEF.AREA_PARENT_IDS or {}
  local rev = {}
  for areaId, name in pairs(pack) do
    if type(name) == "string" and name ~= "" then
      local prev = rev[name]
      if not prev then
        rev[name] = areaId
      else
        local prevParent = parents[prev] or 0
        local curParent = parents[areaId] or 0
        if (prevParent ~= 0 and curParent == 0) or (prevParent == curParent and areaId < prev) then
          rev[name] = areaId
        end
      end
    end
  end
  return rev
end

local function reverseMap(localeCode)
  local cached = reverseByLocale[localeCode]
  if cached then
    return cached
  end
  cached = buildReverse(localeCode)
  reverseByLocale[localeCode] = cached
  return cached
end

local function activeAddonLocale()
  if CEF.Locale and CEF.Locale.getActiveCode then
    return CEF.Locale.getActiveCode()
  end
  return (GetLocale and GetLocale()) or "enUS"
end

--- Traduz o nome de zona devolvido pelo cliente para o idioma ativo do addon.
function CEF.getZoneDisplayName(zoneName)
  if type(zoneName) ~= "string" or zoneName == "" then
    return zoneName or ""
  end
  local addonLoc = activeAddonLocale()
  local clientLoc = (GetLocale and GetLocale()) or addonLoc
  local cacheKey = clientLoc .. "\0" .. addonLoc .. "\0" .. zoneName
  local cached = displayCache[cacheKey]
  if cached then
    return cached
  end
  local out = zoneName
  local areaId = reverseMap(clientLoc)[zoneName]
  if areaId then
    local pack = CEF.AREA_DISPLAY_NAMES[addonLoc] or CEF.AREA_DISPLAY_NAMES.enUS
    local translated = pack and pack[areaId]
    if type(translated) == "string" and translated ~= "" then
      out = translated
    end
  end
  displayCache[cacheKey] = out
  return out
end

function CEF.clearZoneDisplayNameCache()
  wipe(displayCache)
end
